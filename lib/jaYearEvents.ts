/** 干支 (Japanese zodiac) for a given year */
const ETO_NAMES = ["子", "丑", "寅", "卯", "辰", "巳", "午", "未", "申", "酉", "戌", "亥"] as const;
const ETO_ANIMALS = ["ねずみ", "うし", "とら", "うさぎ", "たつ", "へび", "うま", "ひつじ", "さる", "とり", "いぬ", "いのしし"] as const;

export interface EtoInfo {
  kanji: string;   // e.g. "子"
  animal: string;  // e.g. "ねずみ"
}

/** Returns the 干支 for the given year. Formula: (year − 4) % 12 */
export function getEto(year: number): EtoInfo {
  const idx = ((year - 4) % 12 + 12) % 12;
  return { kanji: ETO_NAMES[idx], animal: ETO_ANIMALS[idx] };
}

